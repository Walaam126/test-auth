     */
    public function store(Request $request)
    {
        $request->validate([
            'title'=>['string','min:3'],
            'content'=>['string','min:10']
        ]);
       Post::create([
           'title'=>$request->title,
           'content'=>$request->content,
           'user_id'=>auth()->id(),
       ]);
       return redirect('/posts/index');
    }

